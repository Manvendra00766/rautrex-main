@pytest.mark.asyncio
async def test_pricing_engine_async_httpx_client():
    """Verify that pricing engine executes non-blocking asynchronous requests using HTTPX."""
    symbol = "RELIANCE.NS"
    mock_token = "mock_upstox_token_123"
    
    # Mock dynamic resolve of Upstox keys and active token
    with patch("services.pricing_engine.get_active_upstox_token", return_value=mock_token), \
         patch("services.pricing_engine.resolve_upstox_keys", return_value={symbol: "NSE_EQ|RELIANCE"}), \
         patch("services.pricing_engine.upsert_cached_price", new_callable=AsyncMock):
         
        # High fidelity Upstox response payload
        upstox_payload = {
            "status": "success",
            "data": {
                "NSE_EQ|RELIANCE": {
                    "symbol": "RELIANCE",
                    "last_price": 2450.50,
                    "close": 2400.00,
                    "volume": 1500000,
                    "ohlc": {
                        "close": 2400.00
                    }
                }
            }
        }
        
        # Instantiate httpx Response mock
        mock_response = MagicMock()
        mock_response.status_code = 200
        mock_response.json.return_value = upstox_payload
        
        # Patch httpx.AsyncClient.get to run completely asynchronously
        with patch("httpx.AsyncClient.get", return_value=mock_response) as mock_get:
            snapshot = await get_price_snapshot(symbol, max_age_seconds=0)
            
            # Assert HTTPX AsyncClient was called instead of blocking requests library
            mock_get.assert_called_once()
            call_url = mock_get.call_args[0][0]
            assert "api.upstox.com" in call_url
            
            # Assert response snapshot was constructed correctly
            assert snapshot is not None
            assert snapshot.symbol == symbol
            assert snapshot.last_price == 2450.50
            assert snapshot.previous_close == 2400.00
            assert snapshot.change_amount == 50.50
            assert snapshot.currency == "INR"
            assert snapshot.source == "upstox_quotes_api"


