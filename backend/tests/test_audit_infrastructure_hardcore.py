def test_alembic_configuration_integrity():
    """Verify that Alembic is initialized, env.py dynamically discovers all metadata columns."""
    from alembic.config import Config
    from alembic import script
    
    # Assert alembic.ini is present at root level of backend
    ini_path = "alembic.ini"
    assert os.path.exists(ini_path), "alembic.ini is missing from backend directory"
    
    # Load Config programmatically
    alembic_cfg = Config(ini_path)
    script_directory = script.ScriptDirectory.from_config(alembic_cfg)
    
    # Assert migrations versions folder is configured and initial migration is recorded
    revisions = list(script_directory.walk_revisions())
    assert len(revisions) >= 1, "Initial schema revision has not been generated by Alembic"
    
    # Assert env.py imports metadata correctly
    env_py_path = os.path.join("alembic", "env.py")
    assert os.path.exists(env_py_path), "alembic/env.py is missing"
    with open(env_py_path, "r") as f:
        env_content = f.read()
        assert "target_metadata = Base.metadata" in env_content, "target_metadata is not configured correctly in env.py"
        assert "from models.user_data import" in env_content, "Models are not imported in env.py"


