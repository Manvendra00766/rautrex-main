# AUDIT CONCERN 5: BASE REPOSITORY PURE ASYNC CRUD VALIDATION
# ==============================================================================
@pytest.mark.asyncio
async def test_base_repository_pure_async_crud():
    """Verify BaseRepository operates CRUD queries natively using AsyncSessionLocal rather than synchronous thread executors."""
    # Seed mock portfolio data
    portfolio_id = "port-777"
    user_id = 999
    portfolio_name = "Core Infrastructure Portfolio"
    
    portfolio_record = PortfolioSchema(
        id=portfolio_id,
        user_id=str(user_id),
        name=portfolio_name,
        total_value=250000.0,
        currency="USD"
    )
    
    # Mocking Database AsyncSession context and calls
    mock_db_session = AsyncMock()
    mock_db_session.__aenter__.return_value = mock_db_session
    mock_db_session.add = MagicMock()
    
    # Setup mock portfolio instance
    db_obj = UserPortfolio(id=portfolio_id, user_id=user_id, name=portfolio_name)
    mock_db_session.refresh.return_value = None
    
    # Setup database scalar/execute results
    mock_res_select = MagicMock()
    mock_res_select.scalar_one_or_none.return_value = db_obj
    
    mock_res_select_all = MagicMock()
    mock_res_select_all.scalars.return_value.all.return_value = [db_obj]
    
    mock_db_session.execute.side_effect = [mock_res_select, mock_res_select_all, mock_res_select]
    
    # Patch AsyncSessionLocal constructor to return our mock session
    with patch("database.connection.AsyncSessionLocal", return_value=mock_db_session):
        # 1. Test creation
        created = await portfolio_repo.create(portfolio_record)
        assert created is not None
        assert created.name == portfolio_name
        assert mock_db_session.add.called
        assert mock_db_session.commit.called
        
        # 2. Test async select (get_by_id)
        fetched = await portfolio_repo.get_by_id(portfolio_id)
        assert fetched is not None
        assert fetched.id == portfolio_id
        assert fetched.name == portfolio_name
        
        # 3. Test async select all (get_by_user_id)
        all_portfolios = await portfolio_repo.get_by_user_id(str(user_id))
        assert len(all_portfolios) == 1
        assert all_portfolios[0].name == portfolio_name
        
        # 4. Test async update
        update_data = {"name": "Refactored Core Portfolio"}
        # setup next execute return
        db_obj.name = "Refactored Core Portfolio"
        mock_db_session.execute.side_effect = [mock_res_select]
        
        updated = await portfolio_repo.update(portfolio_id, update_data)
        assert updated.name == "Refactored Core Portfolio"
        assert mock_db_session.commit.called
        
        # 5. Test async delete
        mock_db_session.execute.side_effect = [mock_res_select]
        deleted = await portfolio_repo.delete(portfolio_id)
        assert deleted is True
        assert mock_db_session.delete.called
