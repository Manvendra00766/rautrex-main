# AUDIT CONCERN 3: ROBUST CACHE SERIALIZATION (FASTAPI JSONABLE ENCODER)
# ==============================================================================
class MockCustomEnum(enum.Enum):
    ALPHA = "alpha"
    BETA = "beta"

@pytest.mark.asyncio
async def test_cache_serialization_robustness():
    """Verify that cache_response decorator serializes datetime, custom enum, and decimal types successfully."""
    # Build mock data structure containing complex types that normally fail standard json.dumps
    complex_data = {
        "timestamp": datetime.now(timezone.utc),
        "ratio": Decimal("3.14159"),
        "status": MockCustomEnum.ALPHA,
        "raw_model": {"portfolio_id": 999, "is_active": True}
    }
    
    execution_counter = 0
    
    @cache_response(ttl=30, prefix="test:hardcore")
    async def sample_cached_service():
        nonlocal execution_counter
        execution_counter += 1
        return complex_data
        
    mock_redis = AsyncMock()
    mock_redis.get.return_value = None # Cache miss first
    
    with patch("infrastructure.cache.redis_client", mock_redis):
        # Execute the service
        result = await sample_cached_service()
        
        # Assert database was hit once
        assert execution_counter == 1
        assert result == complex_data
        
        # Verify redis_client.set was invoked with a successfully serialized JSON string
        mock_redis.set.assert_called_once()
        cache_key, serialized_payload, ttl = mock_redis.set.call_args[0]
        
        assert "test:hardcore:sample_cached_service" in cache_key
        # Verify that complex types got serialized successfully (no TypeError)
        decoded = json.loads(serialized_payload)
        assert decoded["status"] == "alpha"
        assert float(decoded["ratio"]) == 3.14159
        assert "timestamp" in decoded


